package Snacks5;

import java.beans.Customizer;

public class Users {
    public int age;
    public String email;
    public String homeAddress;
    public String name;
    public String password;
    public String phoneNumber;

    public Users(int age, String email, String homeAddress, String name, String password, String phoneNumber){
        this.age = age;
        this.email = email;
        this.homeAddress = homeAddress;
        this.name = name;
        this.password = password;
        this.phoneNumber = phoneNumber;
    }
}
